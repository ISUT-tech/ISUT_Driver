package com.isut.cabdriver.user;

public class UserModel {
    String mobile , name ,email,cabnum,cabmodel,lincenseno, location;

    public String getMobile() {
        return mobile;
    }

    public void setMobile(String mobile) {
        this.mobile = mobile;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }


    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getCabnum() {
        return cabnum;
    }

    public void setCabnum(String cabnum) {
        this.cabnum = cabnum;
    }

    public String getCabmodel() {
        return cabmodel;
    }

    public void setCabmodel(String cabmodel) {
        this.cabmodel = cabmodel;
    }

    public String getLincenseno() {
        return lincenseno;
    }

    public void setLincenseno(String lincenseno) {
        this.lincenseno = lincenseno;
    }

    public String getLocation() {
        return location;
    }

    public void setLocation(String location) {
        this.location = location;
    }
}
